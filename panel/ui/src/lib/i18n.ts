const DICTIONARIES: Record<Language, Dictionary> = { zh: ZH, en: EN };

export function translate(language: Language, key: string): string {
  return DICTIONARIES[language][key] || EN[key] || key;
}

export function formatTemplate(template: string, values: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(values[key] ?? ""));
}

export function selectedLanguage(): Language {
  if (typeof window === "undefined") return "zh";
  const stored = window.localStorage.getItem("vps-sentinel-language");
  return stored === "en" ? "en" : "zh";
}
